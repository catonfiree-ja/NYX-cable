// Helper to create simple text blocks
const createBlock = (text, style = 'normal', listItem = undefined) => {
  const block = {
    _key: crypto.randomUUID(),
    _type: 'block',
    style: style,
    markDefs: [],
    children: [
      {
        _key: crypto.randomUUID(),
        _type: 'span',
        marks: [],
        text: text,
      },
    ],
  }
  if (listItem) {
    block.listItem = listItem
    block.level = 1
  }
  return block
}

// Helper to create blocks with bold+normal mixed text
const createMixedBlock = (parts, style = 'normal', listItem = undefined) => {
  const block = {
    _key: crypto.randomUUID(),
    _type: 'block',
    style: style,
    markDefs: [],
    children: parts.map(part => ({
      _key: crypto.randomUUID(),
      _type: 'span',
      marks: part.bold ? ['strong'] : [],
      text: part.text,
    })),
  }
  if (listItem) {
    block.listItem = listItem
    block.level = 1
  }
  return block
}

